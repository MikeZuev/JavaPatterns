package basepatterns.behavioral.command;

public class Robot {

    public void run(){
        System.out.println("Robot is running.");
    }

    public void walk() {
        System.out.println("Robot is walking.");
    }

    public void work() {
        System.out.println("Robot is working.");
    }

    public void charge() {
        System.out.println("Robot is charging.");
    }
}
